// Dato de entrada
let temperatura = 38;

if (temperatura < 5) {
    console.log("Helado 🥶");
}

else if (temperatura < 15) {
    console.log("Frío 🧥");
}

else if (temperatura < 25) {
    console.log("Templado 😎");
}

else if (temperatura < 35) {
    console.log("Caluroso 🥵");
}

else {
    console.log("Infierno 🔥");
}